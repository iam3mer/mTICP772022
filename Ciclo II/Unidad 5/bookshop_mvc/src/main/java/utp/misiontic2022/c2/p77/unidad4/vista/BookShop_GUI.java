    private void saveStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveStockActionPerformed
        // VAlidar que addStock no este vacio
        if (addStock.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "addStock no puede estar vacio!", getTitle(), JOptionPane.WARNING_MESSAGE);
            txtTitle.requestFocus();
        }
        
        try {
            var row = tblBookStock.getSelectedRow();
            Integer id = Integer.valueOf(idBookStock.getText());
            Integer amount = Integer.valueOf(addStock.getText());
            
            Integer newAmount = Integer.valueOf(amountStock.getText()) - amount;
            controlador.updateStock(id, amount);
            
            JOptionPane.showMessageDialog(this, "Se actualizo el stock!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puedo ejecutar la acci√≥n!");
        }
    }//GEN-LAST:event_saveStockActionPerformed

