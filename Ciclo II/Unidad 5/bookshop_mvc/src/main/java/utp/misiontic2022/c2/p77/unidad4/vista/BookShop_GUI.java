    private void btnEliminarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarLibroActionPerformed
        if (JOptionPane.showConfirmDialog(this,
            "Esta seguro de eliminar el libro?\nEl libro solo se va a eliminar si no hay ventas de este!",
            getTitle(),
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE)
        == JOptionPane.YES_OPTION) {

        try {
            var row = tblBooks.getSelectedRow();
            var delete = controlador.deleteBook(Integer.valueOf(idBook.getText()));
            if (delete) {
                booksTM.removeBook(row);
                JOptionPane.showMessageDialog(this, "Se ha eliminado el libro!");
            } else {
                JOptionPane.showMessageDialog(this, "Existen ventas de el libro, no se puede eliminar!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se pudo ejecutar la acci√≥n!", getTitle(), JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnEliminarLibroActionPerformed

