    private void btnSaveBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveBookActionPerformed
        // Validar Campos (title, isbn, year) - Que no esten vacios
        if (txtTitle.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "El titulo es obligatorio!", getTitle(), JOptionPane.WARNING_MESSAGE);
            txtTitle.requestFocus();
        }
        
        if (txtIsbn.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "El ISBN es obligatorio!", getTitle(), JOptionPane.WARNING_MESSAGE);
            txtIsbn.requestFocus();
        }
        
        if (txtYear.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "El a√±o es obligatorio!", getTitle(), JOptionPane.WARNING_MESSAGE);
            txtIsbn.requestFocus();
        }
        
        try {
            String id = idBook.getText();
            String title = txtTitle.getText().trim();
            String isbn = txtIsbn.getText().trim();
            Integer year = Integer.valueOf(txtYear.getText().trim());
            
            if (id.isBlank()) {
                var book = controlador.createBook(title, isbn, year);
                booksTM.addBook(book);
            } else {
                var row = tblBooks.getSelectedRow();
                var book = controlador.updateBook(isbn, title, year);
                book.setId(Integer.valueOf(id));
                booksTM.setBook(row, book);
            }
            
            
            JOptionPane.showMessageDialog(this, "EL libro se guardo en la base de datos!");
            
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_btnSaveBookActionPerformed

