    private void valoresIniciales() {
        try {
            var books = controlador.findAllBooks();
            
            booksTM = new BooksTM(books);
            tblBooks.setModel(booksTM);
            
            tblBooks.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            tblBooks.getSelectionModel().addListSelectionListener((e) -> {
                var row = tblBooks.getSelectedRow();
                if (row == -1) {
                    limpiarCamposLibro();
                    btnEliminarLibro.setEnabled(false);
                } else {
                    var book = booksTM.getBook(row);
                    cargarCamposLibro(book);
                    btnEliminarLibro.setEnabled(true);
                }
            });
            
        } catch (SQLException e) {
        }
        
        try {
            var registrosbs = controlador.consultarStock();
            bookStockTM = new BookStockTM(registrosbs);
            tblBookStock.setModel(bookStockTM);
            
            tblBookStock.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            
            tblBookStock.getSelectionModel().addListSelectionListener((e) -> {
                var row = tblBookStock.getSelectedRow();
                if (row == -1) {
                    limpiarCamposStock();
                } else {
                    var bs = bookStockTM.getBook(row);
                    cargarCamposStock(bs);
                }
            });
            
        } catch (SQLException e) {
        }
        
    }
    
    private void cargarCamposStock(BookStock bs) {
        idBookStock.setText(String.valueOf(bs.getId()));
        amountStock.setText(String.valueOf(bs.getAmount()));
    }
    
    private void limpiarCamposStock() {
        idBookStock.setText("");
        amountStock.setText("");
        addStock.setText("");
    }
    
    private void cargarCamposLibro(Book book) {
        idBook.setText(String.valueOf(book.getId()));
        txtTitle.setText(book.getTitle());
        txtIsbn.setText(book.getIsbn());
        txtIsbn.setEnabled(false);
        txtYear.setText(String.valueOf(book.getYear()));
    }

    private void limpiarCamposLibro() {
        idBook.setText("");
        txtTitle.setText("");
        txtIsbn.setText("");
        txtIsbn.setEnabled(true);
        txtYear.setText("");
    }
    
